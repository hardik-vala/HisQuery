export function generateBase64Id(length: number) {
  const result = ["USR-"];
  for (let i = 0; i < length; i++) {
    result.push(BASE64_CHARS[Math.floor(Math.random() * BASE64_CHARS.length)]);
  }
  return result.join("");
}
